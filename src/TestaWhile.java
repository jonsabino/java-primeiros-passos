public class TestaWhile {
    
    public static void main(String[] args) {
        
        int contador = 0;
        while (contador <= 10) {
            System.out.print(contador + " ");
            //contador += 1; //contador = contador + 1;
            contador++;
        }
    
        System.out.println( " ");
        System.out.println(contador);
    }
}
